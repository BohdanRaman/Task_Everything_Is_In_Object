package task_9;

/**
 * Write a program that demonstrates that autoboxing works for all the
 * primitive types and their wrappers
 */

public class AutoboxingForPrimitiveAndWrapperTypes {
    public byte aByte;
    public short aShort;
    public int anInt;
    public long aLong;
    public double aDouble;
    public float aFloat;
    public char aChar;
    public boolean aBoolean;
    public Byte aByteType;
    public Short aShortType;
    public Integer integer;
    public Long aLongType;
    public Double aDoubleType;
    public Float aFloatType;
    public Character aCharType;
    public Boolean aBooleanType;
}
